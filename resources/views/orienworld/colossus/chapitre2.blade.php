    	<p>- Il est parti affronter un groupe de Terristocrates sur les Hauts Ténébreux de Srambad, intervint Kuro en hochant négativement la tête. Lui et Skaizo ne seront pas disponibles avant plusieurs semaines pour une autre mission. »</p>
    
    	<p>Nouveau silence. La liste des candidats potentiels s’amenuisait à vue d’œil, et je commençais
    	sérieusement à croire que Guenon devrait se débrouiller seul jusqu’à la guérison de Farrent.</p>
    
    	<p>« Yudaï ? appela soudain Kuro. Tu n’as pas prononcé un seul mot depuis le début de la réunion. »</p>
    
    	<p>Tous les regards se portèrent en direction du grand sacrieur qui faisait face à Zoliane à l’autre bout de
    	la table. D’un naturel plutôt discret et timide, il faisait pourtant partie de nos meilleurs éléments
    	lorsqu’il s’agissait de s’exprimer sur un champ de bataille. En matière de combat au corps à corps, Kuro
    	et Yudaï formaient un tandem redoutable. Celui-ci soupira d’un air profondément agacé.</p>
    
    	<p>« Pour être honnête, Kuro, je pensais t’accompagner à Astrub pour assurer tes arrières. Cogner du
    		bontarien et défendre nos percepteurs, ça ne m’intéresse pas. Nous sommes des chasseurs de
    		monstres, toi et moi. Pas des soldats.</p>
    	<p>- Il ne s’agit que d’une mission temporaire, appuya Kuro avec insistance. Un mois peut-être, deux tout au plus. Je suis sûr que Guenon sera ravi de t’avoir à ses côtés.</p>
    
    	<p>Le steamer approuva vigoureusement du chef.</p>
    
    	<p>- Bon, très bien, céda le sacrieur. J’assurerai ce rôle jusqu’à la guérison de Farrent. Kirina, tu peux m’ajouter sur ta liste. »</p>
    
    	<p>Je m’empressai d’obéir et d’inscrire Yudaï au rang de protecteur dans la hiérarchie de la guilde. Pour
    	lui cela représentait une belle promotion, mais il ne semblait pas heureux de l’obtenir. Yudaï était de
    	ces gens discrets et efficaces, qui préféraient agir dans l’ombre plutôt que d’endosser de trop grandes
    	responsabilités. Un choix que je ne comprenais que trop bien.</p>
    
    	<p>« Nous avons presque terminé, déclara Kuro avec un soupir de soulagement bien audible. Il ne reste plus qu’à sélectionner le reste de mon équipe pour le voyage vers Astrub.</p>
    
    	<p>Il marqua une pause très brève, et me fixa droit dans les yeux.</p>
    
    	<p>- Kirina, je veux que tu viennes avec moi. Quelle que soit la nature de cette étrange Pestilence, tes
    	compétences de guérisseuse nous seront précieuses pour l’affronter. »</p>
    
    	<p>Mon cœur rata un battement, et je le sentis s’emballer dans ma poitrine comme s’il allait exploser. Un
    	frisson glacial remonta ma colonne vertébrale, et je serrai les dents pour ne pas me décomposer au
    	beau milieu de l’assemblée. Je me trouvai soudain déchirée entre la joie d’accompagner mon amant
    	pendant les prochaines semaines et une terreur sourde qui me labourait les entrailles. La Pestilence…
    	L’espace d’un instant, je revis très clairement dans mon esprit le bras violacé de Merise, et la nécrose
    	qui progressait en direction de son cœur. Que m’arriverait-il si cette chose entrait en contact avec moi
    	pendant que je m’occupais des blessés ? Kuro dut entrevoir la peur qui assaillait mes pensées, car il
    	m’adressa un sourire bienveillant et ajouta :</p>
    
    	<p>« Tu n’es pas obligée de venir, tu sais. Je comprendrais que ce soit au-dessus de tes forces. Nous ne
    	savons rien de cette étrange maladie, ni de la manière dont elle contamine ses victimes.</p>
    	<p>- Non, bredouillai-je d’une voix mal assurée. Tout va bien. Je suis prête. Tu peux compter sur moi. »</p>
    
    	<p>Son sourire s’élargit un peu plus, et l’étau qui comprimait mon cœur se relâcha aussitôt. Sa voix grave
    	et chaude avait sur moi un effet enivrant que je ne parvenais pas à décrire. Kuro était de ces gens qui,
    	grâce à leur charisme et leur force de caractère, transformaient n’importe quelle menace en une
    	aventure palpitante. Avec lui, nul n’hésiterait à braver les flammes ardentes du Mont Torrideau ou à
    	parcourir les profondeurs abyssales de l’ancienne Sufokia. Même la fin du monde, j’en étais
    	convaincue, me paraîtrait douce et rassurante si je pouvais la vivre au creux de ses bras.</p>

    	<p>« Nous emmèneront également Sheicko avec nous, ajouta-t-il en retrouvant sa posture de chef. Deux eniripsas vaudront mieux qu’un pour un périple comme celui-ci. Et puisque Yudaï restera défendre nos
    		percepteurs, nous prendrons Marsh à nos côtés.</p>
    	<p>- Marsh ? Intervint Diavolin d’un air étonné. N’est-il pas un peu novice pour une mission aussi dangereuse ?</p>
    	<p>- Ses compétences sont moindres en matière de combat, je te l’accorde. Mais Yudaï est un excellent
    		professeur, et Marsh a étudié au Temple de Sacrieur pendant son adolescence. Il connait les rituels de
    		purification du sang, et c’est un excellent alchimiste. Il aidera Sheicko et Kirina à élaborer un antidote
    		contre cette Pestilence.</p>
    	<p>- Tu marques un point, reconnut le pandawa. Mais si on se fie au récit d’Elito et au témoignage de Merise, quelque-chose a attaqué et enlevé nos amis dans ce désert. Il te faudra sans doute mieux qu’un sacrieur prépubère et un vieux débris d’enutrof dans ta troupe de guerriers. »</p>
    
    	<p>Je m’esclaffai sous cape et jetai discrètement un regard en direction de Kamen, qui maugréait dans sa
    	barbe. Diavolin et lui passaient leur temps à s’asticoter, à tel point que c’était devenu un jeu très
    	populaire au sein de notre famille. Bon nombre de nos amis pariaient à qui ferait mouche avec le plus
    	de répliques.</p>
    
    	<p>« Tu as raison, confirma Kuro d’une voix grave. Nous devons être en mesure d’affronter ce qui se terre derrière ce portail Zaap. Je pense que Celexane sera une excellente recrue pour m’épauler en cas de
    		besoin. Elle est un peu tête-brûlée pour une jeune iop, mais sa formation est presque achevée. Et elle
    		ne manque pas de cran, pour une gamine.</p>
    	<p>- Je doute qu’Entrail l’autorise à prendre part à une expédition aussi dangereuse, souligna Ori. Il est très protecteur avec sa fille.</p>
    	<p>- Nous en avons déjà discuté ce matin, répondit Kuro. J’ai croisé Entrail en revenant de la Tour, un peu avant que tu me rejoignes. Il est d’accord pour se joindre à nous. Ses pouvoirs de Feca tiendront à
    		l’écart les éventuelles menaces, et il pourra directement veiller sur Celexane. Je lui ai fait la promesse
    		qu’en cas de danger, ma priorité absolue sera de protéger sa fille. »</p>
    
    	<p>Ori approuva d’un signe de tête, et je repris ma plume pour ajouter ces deux noms à la liste des
    	volontaires. Nous étions désormais sept pour affronter ce périple vers les profondeurs d’Astrub : Kuro
    	serait le chef de l’expédition, épaulé par Kamen l’enutrof et Celexane la jeune iop. En tant que disciple
    	de la déesse Feca, Entrail servirait de protecteur pour les membres du groupe. Venaient ensuite Marsh
    	et Sheicko, nos deux alchimistes : leur formation les prédisposait à rester dans le Sanctuaire de Martalo
    	pour trouver un remède à la Pestilence. Quant à moi, je serai à la fois le héraut chargée du récit de nos
    	découvertes, et la deuxième guérisseuse susceptible de prêter main forte à Sheicko en cas de besoin.
    	Ma plume gratta le parchemin une dernière fois, et je me redressai d’un air satisfait. En dépit de la
    	blessure de Farrent qui forçait Yudaï à rester sur la touche, Kuro avait réussi à constituer une équipe
    	cohérente et efficace. Seule ombre au tableau, la présence indispensable de Merise qui devait nous 
    	servir de guide et faire un rapport à Oto Mustam à notre retour. L’idée de passer mes prochaines
    	semaines aux côtés de cette sramette me déplaisait affreusement.</p>
    
    	<p>« Nous en avons donc fini pour ce matin, conclut Kuro en quittant sa chaise. Ceux qui restent à Brakmar devront épauler Elito dans son enquête et faire fonctionner nos boutiques pendant notre absence. Comme d’habitude, Ori assurera le rôle de meneur de guilde jusqu’à mon retour.</p>
    
    	<p>Il sourit, porta un toast et lança à la cantonade avec entrain :</p>
    
    	<p>- Le Conseil est terminé ! Jespa, apporte-nous ton sanglier à la broche ! »</p>
    
    	<p>C’était le top départ de nos festivités. Guenon éclata de rire et s’empressa de se lever pour aller
    	chercher des assiettes. L’osamodas se hâta de remettre en route sa rôtissoire pour réchauffer la
    	viande, et le brouhaha familier des conversations ne tarda pas à emplir de nouveau la grande salle.
    	Toutefois, je n’avais pas vraiment le cœur à reprendre mes instruments ou à chanter. Mon esprit
    	voguait par-delà les mers et les montagnes, jusqu’à la cité des mercenaires et à l’entrée obscure de
    	ses mines, où un mal étrange et inconnu nous attendait dans l’ombre. Je frissonnai, et mon regard se
    	porta en direction de Merise, qui n’avait pas bougé de son siège.</p>
    
    	<p>Elle m’adressa un sourire féroce, et leva lentement sa coupe à mon intention.</p>
