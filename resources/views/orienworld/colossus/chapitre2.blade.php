        <h2 class="titre-chapitre">Chapitre 2 - Le conseil de guilde</h2>
    
    	<p>Il faisait froid ce matin-là dans les ruelles sombres de Brakmar, et l’aube n’était pas encore levée. Les
    	éternels flambeaux qui illuminaient la ville ne consumaient plus que quelques braises, diffusant une
    	pâle lueur maladive. Ori souffla dans ses mains pour se réchauffer un peu et resserra les pans de sa
    	cape sur sa poitrine. Un vent mordant balayait la grande place de la milice, encore déserte à cette
    	heure à l’exception d’une poignée de gardes et d’une colonie de sousouris. Kuro était en retard, ce
    	n’était pas dans ses habitudes.</p>
    
    	<p>Enfin, après de longues minutes d’attente, une silhouette encapuchonnée se détacha au nord de la
    	banque, en provenance du quartier de la Tour. Il marchait d’un pas vif, une torche ardente dans la
    	main, et semblait pressé de regagner le couvert d’un bâtiment. Lorsqu’il l’aperçut, Ori lui adressa de
    	grands gestes.</p>
    
    	<p>« Kuro ! Par ici ! »</p>
    
    	<p>Le iop l’entendit, et obliqua dans sa direction. Il ne lui fallut que quelques foulées pour rejoindre
    	l’huppermage, qui s’abritait de la pluie sur le pas d’une porte.</p>
    
    	<p>« Salut Ori, dit-il d’un ton fatigué. Tu es bien matinal.</p>
    	<p>- Jespa m’a informé de la convocation. Tu as pu obtenir plus de détails ?</p>
    
    	<p>Kuro acquiesça, et jeta un regard inquiet autour de lui.</p>
    
    	<p>- Pas ici, murmura-t-il. On en parlera au Conseil. »</p>
    
    	<p>Ils reprirent la route, côte à côte dans l’obscurité mourante. Le iop dominait Ori de presque deux têtes,
    	et il avait incontestablement la carrure d’un guerrier. Même sous son manteau épais, on devinait en
    	le regardant des épaules larges, des bras musculeux et la taille élancée de celui qui faisait
    	régulièrement de l’exercice. Ce n’était un secret pour personne dans la Colossus : Kuro se relevait
    	fréquemment au milieu de la nuit pour se rendre au kanojedo afin de s’entraîner. Contrairement à
    	Orimage, il faisait partie des combattants ; sous son épée, le terrible Bworker avait plusieurs fois mordu
    	la poussière lorsqu’il tentait d’assiéger Brakmar avec ses légions. Kuro ne craignait pas d’arpenter seul
    	les terres désolées du Berceau d’Alma sur Frigost, pourtant peuplées de sinistres fantômes. Quelques
    	semaines plus tôt, il avait monté une expédition victorieuse pour repousser une horde de Trépamorts
    	dans les Marches Magmatiques, et les renvoyer en Externam. Et, à en croire le contenu de la
    	proclamation, son courage ne tarderait pas à être mis de nouveau à l’épreuve.</p>
    
    	<p>« Tu as encore passé la nuit sur tes enclumes, pas vrai ? »</p>
    
    	<p>Ori lança à son équipier un regard distrait. Kuro souriait sous son capuchon. Il n’attendait pas vraiment
    	de réponse à sa question : les deux amis se connaissaient depuis si longtemps qu’ils n’avaient pas
    	besoin de parler pour se comprendre. Ils étaient presque comme deux frères, à la fois différents mais
    	extrêmement proches.</p>

    	<p>« Je terminais une commande urgente, prétendit l’huppermage en haussant les épaules.</p>
    	<p>- À d’autres ! Nous savons tous les deux que tu aurais pu achever cet arc il y a trois jours. Tu es trop
    	perfectionniste, Ori, et trop modeste pour oser l’avouer.</p>
    	<p>- Possible. »</p>
    
    	<p>Il se turent tandis qu’ils traversaient une rue déserte en direction du quartier général de la guilde. La
    	Colossus s’était installée dans une haute masure à la façade sombre, dont les fenêtres luisaient d’une
    	lumière jaunâtre au travers d’un verre crasseux et délavé. Une vieille enseigne bancale qui oscillait
    	sous le vent rappelait aux riverains que cet endroit, autrefois, avait servi à héberger un ordre de
    	mercenaires. Désormais, les murs étaient couverts par deux grandes bannières aux armoiries de la
    	guilde, écusson de gueule habillé d’un vol d’argent. En dépit de son apparence extérieure, l’endroit
    	était confortable et bien situé, à proximité immédiate de la milice et de l’atelier des forgemages où
    	travaillaient Ori et Kamen depuis plusieurs années.</p>
    
    	<p>En pénétrant à l’intérieur, ils eurent soudain l’impression de plonger dans un autre monde.
    	Kuro et Ori abandonnèrent sur le pas de la porte la pluie, le vent glacial et le silence qui s’étaient
    	abattus sur la cité brakmarienne. Le quartier général de la Colossus était un endroit vivant et
    	chaleureux, où résonnaient les rires et le son cristallin d’une harpe. Ici, le sol de pierre se parait de
    	tapis aux multiples couleurs ; une immense table en chêne avait été dressée au centre de la pièce, en
    	face d’un feu ronflant qui crépitait joyeusement dans l’âtre. De toute évidence, Jespa avait trouvé le
    	temps d’y mettre un sanglier en broche, et une douce odeur de viande rôtie ne tarda pas à emplir
    	l’atmosphère. Qu’importe l’heure matinale et le soleil tout juste levant : les réunions de la guilde se
    	déroulaient toujours autour d’un généreux banquet. Le partage avant l’ouvrage, telle était la tradition
    	de notre famille depuis sa fondation.</p>
    
    	<p>Lorsque les deux amis parurent dans le couloir d’entrée, je me tenais assise dans un canapé
    	confortable, pinçant distraitement les cordes de ma nouvelle harpe que Kamen avait installée devant
    	moi. C’était un instrument magnifique qu’Ori m’avait offert quelques semaines plus tôt : cordée en
    	boyaux de bouftou surmontés de crochets en corne, elle avait été sculptée dans un bois clair verni à la 
    	graisse de porkass, qui reflétait la lueur des flammes comme de l’ivoire poli. Son arc cintré faisait
    	presque ma taille, et un ingénieux dispositif de pédales me permettait de modifier la longueur des
    	cordes pour en tirer de nouvelles sonorités. J’avais choisi ce matin-là de jouer une ballade aux accents
    	exotiques de la cour de Sufokia, un air léger et entrainant qui rythmait de ses tonalités chantantes les
    	préparatifs du repas. En m’apercevant à l’autre bout de la salle, Kuro me fit de grands gestes et se
    	précipita vers moi.</p>
    
    	<p>« Kirina ! S’exclama-t-il d’un ton enjoué en me serrant dans ses bras. Comment va notre musicienne
    	préférée ? Sang-de-Rushu, ça fait si longtemps !</p>
    
    	<p>J’éclatai de rire tandis qu’il me soulevait au-dessus du sol dans une étreinte forte et passionnée.</p>
    
    	<p>- Arrête, Kuro, tu vas finir par m’étouffer ! » lui dis-je en riant.</p>
    
    	<p>Le grand iop me reposa délicatement sur le coin de la banquette, non sans déposer au coin de mes
    	lèvres un baiser discret. Nous étions ensemble depuis près de deux mois, mais ne l’avions pas encore
    	annoncé au reste de notre fraternité.</p>

    	<p>« Comment s’est passé ton voyage à la cour d’Allister ? m’interrogea-t-il en me dévorant des yeux. Et
    	ce concours de poèmes dont tu m’avais parlé ?</p>
    	<p>- J’ai fini à la seconde place, encore une fois. Majestueux a vraiment une plume incroyable. »</p>
    
    	<p>Kuro acquiesça sans vraiment paraître surpris. Il était de notoriété publique que le ménestrel de la
    	guilde Yume Ohana était le plus doué de sa génération. Beaucoup le comparaient même au célèbre
    	Francisque Kabroule, qui était autrefois le chanteur attitré du roi.</p>
    
    	<p>« C’est déjà une belle performance, me félicita Ori qui venait de nous rejoindre. Et je suis certain que
    	tu feras mieux l’an prochain. »</p>
    
    	<p>Je lui adressai un sourire enjôleur, avant de retourner m’installer derrière mon instrument. À l’intérieur
    	de la grande salle, le rire gras et les plaisanteries douteuses de Guenon succédaient à ma mélopée
    	pleine de charme, et je ne pouvais pas laisser ce gredin de steamer me damer le pion. Sans cesser de
    	prêter une oreille attentive aux conversations, je repris donc possession de l’ambiance musicale des
    	lieux.</p>
    
    	<p>« Je te jure que c’est vrai ! S’écria Diavolin un peu plus loin, assis devant le feu sur un tabouret.</p>
    
    	<p>Le pandawa devisait gaiement avec Kamen et Jespa, et leur entretien semblait très animé.</p>
    
    	<p>- Un tremble ensorcelé ! Reprit le maître bucheron en faisant de grands gestes. Je l’ai vu comme je
    	vous vois ! Cette saleté d’arbre a pris vie au premier coup de hache, et s’est jeté sur mes apprentis ! Le 
    	pauvre Farrent a voulu les protéger, il a reçu une branche en pleine poitrine qui a bien failli lui briser
    	la cage thoracique !</p>
    	<p>- Un arbre guerrier ! Ricana Jespa en sirotant un verre. Bah voyons ! Tu es sûr que tu n’avais pas un
    	peu forcé sur la boisson, pendant tes libations à la déesse ?</p>

    	<p>Le pandawa se renfrogna, visiblement choqué.</p>

    	<p>- Je ne bois jamais plus d’un tonneau par jour, et deux à la période sacrée. J’étais pratiquement sobre !</p>
    	<p>- C’est ça, renchérit Kamen avec un sourire mesquin. Foi d’Enutrof, si j’avalais une telle quantité de lait
    	de bambou, je serais rond comme une queue de pelle ! »</p>
    
    	<p>Tous trois partirent d’un grand éclat de rire, et Jespa leur resservit un fond de liqueur tandis que Kamen
    	pressait Diavolin de raconter la suite. Aussi incroyable que puisse paraître son histoire, elle était
    	pourtant vraie. Etant l’une des trois eniripsas de la guilde, j’étais bien placée pour le savoir : je m’étais
    	occupée moi-même des nombreuses contusions de ce pauvre Farrent. Le tremble agressif lui avait
    	pratiquement perforé un poumon.</p>
    
    	<p>« N’empêche qu’il se passe des choses anormales sur cette île ! intervint Guenon qui s’approchait, un
    	verre à la main. Cet hiver perpétuel ne me dit rien qui vaille, et toutes ces histoires de fantômes et de
    	Givrefoux dans les collines me donnent des frissons. Je ne suis pas fâché d’avoir quitté la Bourgade
    	pour venir m’installer ici avec vous. »</p>
    
    	<p>Le steamer attrapa une chaise à dossier et se jeta négligemment en travers, les yeux perdus dans la
    	contemplation du feu de cheminée. En l’apercevant, Kuro se détourna de ma musique pour aller
    	l’interpeller.</p>

    	<p>« Des nouvelles du Temple des Alliances, Guenon ? Comment vont nos affaires sur le continent ?</p>
    	<p>- Plutôt bien, je dirais. Deux de nos percepteurs ont été agressés cet été par une patrouille de
    	bontariens, mais Farrent leur a infligé une sacrée correction. On contrôle toujours la péninsule, et nos
    	réserves de gelées sont pleines à craquer pour l’hiver.</p>
    	<p>- Parfait. Diavolin, où en sont nos entrepôts sur la côte ? »</p>
    
    	<p>Le pandawa interrompit le récit de son combat contre le tremble envoûté pour faire un compte-rendu
    	détaillé à Kuro concernant les stocks de bois. La situation était loin d’être bonne de ce côté : les
    	prédictions de la famille Ax avaient libéré dans les forêts une déferlante de bucherons amateurs qui
    	espéraient gagner quelques Kamas en coupant tout ce qui avait des feuilles et des branches. Les jeunes 
    	pousses et les arbrisseaux se retrouvaient malheureusement étêtés avant l’âge des premiers
    	bourgeons, et le maître-bucheron de la guilde redoutait une pénurie généralisée sur tout le continent.</p>
    
    	<p>« Le frêne va se négocier à prix d’or dans tous les Hôtels de Vente l’an prochain, commenta Ori d’un
    	ton exaspéré. Il faudra dédommager nos sculpteurs de leur baisse d’activité.</p>
    	<p>- Je croyais qu’on allait faire une opération d’achat avec les fonds de la guilde ? s’enquit Kamen, l’œil
    	brillant de malice. Il y a un joli bénéfice à la clé, ne l’oublions pas !</p>
    	<p>- Nous ferons de notre mieux, Kamen. Mais même en admettant que nos entrepôts soient pleins, il
    	faudra tout de même rationner les réserves. Diavolin fournit environ un quart des guildes de Brakmar,
    	et je suis sûr qu’Oto Mustam réquisitionnera un bon tiers de notre marchandise. »</p>
    
    	<p>Orimage se tut, et je choisis cet instant pour mettre fin à ma ballade sufokienne, que je remplaçai
    	aussitôt par les tons graves et puissants d’une marche brakmarienne exécutée sur mon hautbois.
    	Interpellé, Kuro m’adressa un coup d’œil interrogatif : c’était là un rythme martial que je réservais
    	d’ordinaire au départ des expéditions militaires sur le continent, bien loin des musiques enjouées et
    	dansantes de mon répertoire habituel. Il comprit cependant la raison de mon choix quand des pas
    	lourds et des claquements métalliques retentirent dans le couloir d’entrée. Simultanément, tous les
    	occupants de la grande salle se retournèrent.</p>
    
    	<p>Le nouveau-venu était un homme au visage sévère, à la peau mate et au menton couvert d’une barbe
    	grise taillée en pointe. Il était revêtu d’une armure sombre galonnée de tissu écarlate, et portait dans
    	sa main une hallebarde noire qui résonnait sinistrement sur le sol à chacun de ses pas. À sa ceinture
    	pendaient côte-à-côte un large sabre de cavalerie et un crâne de chafer en obsidienne, symbole de son
    	rang : il s’agissait d’Oto Mustam, maître de la milice et gouverneur de la ville de Brakmar. À son
    	approche, tous se levèrent pour le saluer d’un poing sur le cœur, et une cohorte de soldats se déploya
    	à ses côtés. Je m’apprêtais moi aussi à luirendre hommage lorsqu’il me fit signe distraitement de rester
    	assise et de poursuivre ma musique. Les guisarmiers se mirent en position de garde-à-vous, et Oto
    	Mustam confia sa hallebarde à un fantassin en grognant. Il fit alors un dernier pas solennel dans notre
    	direction.</p>
    
    	<p><strong>« Par la toute-puissance de Djaul, à quoi riment ces festivités ?!</strong> Aboya-t-il à la cantonade.<strong> Meneurs de guilde, au rapport ! »</strong></p>
    
    	<p>Un vent de panique balaya l’atmosphère, et plusieurs de mes camarades se recroquevillèrent. Kuro le
    	rejoignit d’une démarche hésitante, baissant les yeux en signe de soumission. Il blêmit, et attendit sans
    	coup férir la remontrance qui allait s’ensuivre. La scène aurait pu en faire sourire plus d’un si elle n’était 
    	pas empreinte d’une telle gravité : le iop dépassait le maître de la milice de presque deux têtes et le
    	dominait de sa carrure imposante. Pourtant, il s’inclina devant lui et courba l’échine, comme l’exigeait
    	le protocole. Orimage soupira et imita son camarade.</p>
    
    	<p><strong>« J’ai confié une mission à la Colossus,</strong> reprit Mustam d’une voix tremblante de colère.<strong> Et je vous retrouve occupés à préparer un banquet ! Dites-moi, meneurs de guilde, avez-vous constitué une équipe pour braver les sables du désert, comme vous en avez reçu l’ordre ?!</strong></p>
    
    	<p>Kuro déglutit, et prit le temps de choisir soigneusement sa réponse.</p>
    
    	<p>- Je reviens tout juste de la Tour où je me suis entretenu avec le général Krakdel, Maître. Je passais en
    	revue les affaires de notre guilde lorsque vous êtes arrivé. Le Conseil va bientôt débuter.</p>
    	
    	<p><strong>- Je me contrefiche de vos histoires stupides et de vos échanges commerciaux ! </strong>explosa le maître de Brakmar.<strong> Je vous ai transmis un ordre, bande de minables, alors exécution ! »</strong></p>
    
    	<p>Il claqua dans ses doigts d’un geste sec, et aussitôt deux miliciens s’écartèrent pour laisser paraître une
    	sramette vêtue de pourpre et un ecaflip en tenue d’apparat. Celui-ci adressa un signe discret aux
    	membres de la guilde, et alla s’appuyer contre le montant de la cheminée. Il s’agissait d’Elito, meneur
    	d’une autre fraternité brakmarienne et principal concurrent de la boutique d’Orimage.</p>
    
    	<p><strong>« Je vous présente Merise, votre nouveau membre ! </strong>Reprit Oto Mustam en désignant la sramette.<strong> Elle rejoint votre guilde de manière permanente à compter d'aujourd'hui. C’est l’une de nos meilleurs agents de renseignement. Elle vous servira de guide au cours de votre expédition.</strong></p>
    
    	<p>Il marqua une pause, jetant un regard dédaigneux sur les deux amis agenouillés devant lui. Ses yeux
    	brillaient d’une lueur mauvaise.</p>
    
    	<p><strong>- Vous avez une heure pour sélectionner vos guerriers et les présenter à la milice. Ne soyez pas en retard,
    	sinon... »</strong></p>
    
    	<p>Il laissa planer un silence lourd de sens. Je frissonnai, et mes doigts raides manquèrent un accord le
    	long de mon hautbois. On aurait dit qu’un carcan glacial venait de s’abattre sur toute la grande salle.
    	Les dernières notes de ma marche brakmarienne moururent dans un soupir d’agonie. Mustam renifla
    	avec dédain, et fit signe à ses soldats de quitter les lieux. Comme un seul iop, ils formèrent une colonne,
    	et s’en furent à travers le hall d’entrée en claquant des talons. Un courant d’air froid s’insinua parmi
    	nous lorsqu’ils sortirent, et Oto Mustam emboîta le pas à ses laquais. La porte claqua finalement, et
    	l’atmosphère parut aussitôt s’alléger un peu.</p>
    
    	<p>« Nom d’un Kama en toc ! s’exclama Kamen d’une voix blanche. Ce type est plus effrayant qu’une armée de Trépamorts ! »</p>
    
    	<p>Personne n’osa relever son commentaire, de peur que le maître de la milice soit encore dans les
    	parages. Diavolin lui asséna un coup de coude et désigna sans un mot la sramette qui patientait, debout
    	contre le mur. Terminés, les rires et les conversations sonores qu’accompagnaient le crépitement du
    	feu ; tous nos regards étaient désormais braqués en direction de cette inconnue qui semblait nous
    	juger un par un avec dédain. Finalement, ce fut Kuro en personne qui rompit le silence, brisant du
    	même coup la méfiance que l’on sentait planer dans la pièce. Pour l’encourager, je décidai de jouer un
    	nouvel adagio de ma composition. Mes doigts ne pinçaient plus les cordes de ma harpe avec force
    	comme précédemment : je me contentai cette fois de les caresser avec délicatesse, créant une mélodie
    	douce et rassurante dont les tonalités exotiques n’étaient pas sans rappeler les chants traditionnels de
    	Pandala.</p>
    
    	<p>« Bon, vous avez tous entendu le vieux Mustam, lança Kuro à la cantonade. Je crois que ton sanglier rôti devra attendre, Jespa. Que chacun rejoigne sa place, nous allons commencer. »</p>
    
    	<p>Il ne fallut que quelques instants à mes amis pour se répartir tout autour de la longue table, à
    	l’extrémité de laquelle un haut siège de bois poli demeurait vide. Elito était un habitué des lieux, et
    	une place lui était réservée du côté de la cheminée. Bien qu’il ne fasse pas partie de notre guilde à
    	proprement parler, il était l’un de nos partenaires commerciaux privilégiés et assistait souvent à nos
    	réunions. Ori et lui se partageaient le marché de la forgemagie depuis des années, et la rivalité qui
    	existait autrefois entre eux s’était peu à peu changée en une solide amitié. Désormais, les artisans de
    	la Colossus fournissaient chaque semaine l’atelier d’Elito en matières premières, et celui-ci en retour
    	cédait une partie de son stock de runes à l’échoppe d’Orimage pour un bon prix. C’était un ecaflip de
    	haute stature au regard rieur et au poil luisant, dont le sens du négoce rivalisait avec les talents de
    	Kamen en la matière. Un mois plus tôt, Oto Mustam avait nommé Elito contrôleur des hôtels de vente
    	et des places marchandes de la ville de Brakmar, ce qui faisait de lui un haut responsable du commerce
    	dans la cité sombre. Rusé, intelligent et excellent orateur, il remplissait ses nouvelles fonctions avec
    	brio. Mais il ne faisait pas partie de la caste des guerriers, et sa présence ici pour discuter d’une
    	expédition militaire m’intriguait au plus haut point. Je lançai donc un regard interrogateur en direction
    	de mon amant, qui se contenta de hocher la tête en silence. De toute évidence, Kuro avait l’intention
    	de laisser Elito s’expliquer pendant le Conseil. A regret, je délaissai ma harpe et ma musique pour
    	m’emparer d’un rouleau de parchemin et d’une plume de Haute-Truche. Etant la plus lettrée de nos
    	compagnons, le rôle de secrétaire me revenait systématiquement.</p> 
    
    	<p>« Bien ! s’exclama Kuro lorsque tous furent confortablement installés au fond d’une chaise. Je déclare donc ouvert le dix-neuvième Conseil de la guilde Colossus. Avant d’annoncer l’ordre du jour, j’aimerais inviter Merise à prendre place parmi nous. »</p>
    
    	<p>Tous les regards se tournèrent de concert vers la sramette, qui s’avança d’un pas discret mais
    	autoritaire vers l’extrémité de la table. A mon grand étonnement, elle s’empara du siège réservé au
    	fondateur de notre petite famille, et se laissa tomber dessus avec un grognement. Kamen blêmit d’un
    	air outré, et Guenon lui lança un regard noir. Pourtant, ni Ori ni Kuro ne réagirent à cette provocation,
    	et l’huppermage lui adressa même un léger sourire. Depuis bientôt deux ans, le créateur de Colossus
    	avait choisi de se mettre en retrait des affaires de la guilde, mais la tradition voulait qu’on lui réserve
    	son fauteuil et qu’il demeure inoccupé jusqu’au jour de son éventuel retour. Personne, depuis son
    	départ, n’avait osé s’y installer. Mais la dénommée Merise représentait Oto Mustam, et nul ne pouvait
    	s’élever contre l’autorité du terrible maître de Brakmar sans en subir les conséquences. Je ravalai donc
    	la réplique cinglante qui me brûlait les lèvres, et me forçai à afficher un air faussement décontracté.</p>
    
    	<p>« Kirina ? Me glissa Diavolin à voix basse. L’ordre du jour… ? »</p>
    
    	<p>Je me râclai la gorge et détournai mon attention de l’étrangère qui avait osé s’attribuer la place
    	d’honneur. Quelque-chose chez cette Merise me déplaisait, un sentiment de méfiance et de rejet
    	viscéral comme je n’en avais jamais eu auparavant. Mon instinct me hurlait de ne pas m’approcher
    	d’elle, de la forcer à quitter les lieux séance tenante. Pourtant, je n’en fis rien, et me contentai à la
    	place de jeter un œil sur le gros registre de cuir que l’on avait déposé près de moi.</p>
    
    	<p>« Le Conseil de guilde devra traiter ce matin de l’expédition ordonnée par le général Krakdel, et former
    	un groupe de guerriers pour mener à bien cette mission, dis-je d’une voix forte. Une requête a
