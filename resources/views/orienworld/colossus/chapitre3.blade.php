        <h2 class="titre-chapitre">Chapitre 3 - L'heure du départ</h2>
    
        <p>La pluie avait cessé de tomber lorsque nous sortîmes tous ensemble du quartier général.</p>
        
        <p>Ce matin-là, Brakmar avait des allures de ville fantôme que je ne lui connaissais pas. D’ordinaire, la cité sombre
        grouillait d’activité au lever du jour : c’était l’heure à laquelle les commerçants ouvraient leurs
        échoppes et installaient leurs étals sur la place marchande. De nombreux aventuriers en quête de
        gloire se bousculaient devant les hôtels de vente, et d’autres moins admirables cherchaient
        désespérément un lieu à même de satisfaire l’étendue de leurs vices. Car on trouvait de tout au cœur
        de l’immense cité de Djaul, à condition de connaître les personnes à qui s’adresser. Les arrièreboutiques abritaient fréquemment des magasins
        clandestins où s’échangeaient sous le manteau quantité de poisons, de mixtures aphrodisiaques et d’armes enchantées aux pouvoirs maléfiques. Ici,
        les tristement célèbres Shushus – ces objets possédés par un démon capable de corrompre l’esprit de
        leur propriétaire – passaient pour des pièces de collection exceptionnelles, et les maisons closes
        signalées par une lanterne rouge accueillaient bien plus de visiteurs que les musées. Oui, Brakmar était
        plus que jamais la ville de la luxure, du meurtre et de la débauche, façonnée à son image par le
        redoutable tyran qui la dirigeait d’une main de fer. Car ce n’était plus un secret pour personne
        désormais : Oto Mustam avait renversé le Triumvirat qui gouvernait jadis notre cité et régnait ici en
        maître absolu.</p>
        
        <p>Pourtant, malgré tous ses défauts et cette sinistre façade qu’elle affichait fièrement, la capitale des
        démons était un endroit prospère et familier pour ceux d’entre nous qui avaient appris à arpenter ses
        ruelles. Depuis plus de quinze ans, je m’y sentais chez moi auprès de notre guilde. Les grandes artères
        et les coupe-gorges plongés dans l’obscurité ne m’effrayaient plus depuis longtemps, car les
        détrousseurs et autres vauriens qui sévissaient ici avaient appris à craindre le blason de ma famille.
        Grâce au talent de ses artisans et au courage de ses guerriers, la Colossus s’était hissée au rang des
        guildes protégées par le pouvoir d’Oto Mustam ; quiconque oserait s’en prendre à l’un de ses membres
        serait puni de mort et exécuté sans sommation. Tel fut d’ailleurs le triste sort de Zack le Ventreur, un
        assassin sram engagé pour éliminer Kuro l’année passée. Le pauvre bougre rata sa cible de plus d’un
        mètre car la pluie diluvienne qui s’abattait ce jour-là avait détrempé la corde de son arc. Kuro et Yudaï
        n’eurent aucun mal à le neutraliser, et il fut conduit à la milice devant le vieux Mustam, qui le
        condamna au supplice. Lui qui aimait tailler des sourires sanglants sur le ventre de ses victimes reçut
        le même châtiment, avant d’être suspendu par les poignets au-dessus des douves pleines de lave de
        la cité. Il fut abandonné ainsi pendant plusieurs semaines, et lorsqu’Oto Mustam autorisa finalement les gardes à récupérer son corps, ses os
        desséchés par la fournaise étaient devenus friables et tombaient en poussière.</p>

        <p>Oui, plus que jamais, Brakmar était mon foyer et mon point de repère. Grâce à mon adhésion dans
        l’Académie des Bardes du roi Allister, je passais le plus clair de mon temps à parcourir le Monde des
        Douze pour chanter, diffuser les nouvelles et enrichir mon répertoire. Pourtant, il n’existait aucun
        endroit capable de me réconforter autant que lorsque j’apercevais dans le lointain les hautes murailles
        de la cité sombre. Ma place était ici, entourée de ma famille.</p>

        <p>« Tu es bien songeuse ce matin, Kirina. Ce n’est pas dans tes habitudes. »</p>
        
        <p>Je m’arrachai à mes pensées pour adresser un sourire timide à Kamen qui marchait à mes côtés. Après
        avoir dévoré une large part de cuissot de sanglier, le vieil enutrof s’était changé intégralement,
        abandonnant son tablier de forgemage pour une tenue de voyage plus décontractée en cuir de tabi. Il
        s’était chaussé de solides bottes-honymes, et portait sur son crâne dégarni un large solomonk tressé
        en fibres de bambou. Ses yeux gris clair pétillaient de malice et il paraissait avoir rajeuni de trente ans.
        Son attirail aussi lui donnait l’allure d’un vétéran, avec sa large pelle posée au-dessus de son épaule et
        sa fidèle musette en bandoulière. Tout en m’observant, il jonglait négligemment avec un Kama
        d’argent, qu’il s’amusait à envoyer en l’air et à rattraper entre son pouce et son index. Je souris de plus
        belle en reconnaissant là sa pièce porte-bonheur, qui n’avait pas quitté le coffre de son havre-sac
        depuis sa retraite anticipée quatre ans plus tôt. Kamen se plaisait à dire que le Héraut d’Enutrof en
        personne la lui avait offerte, lors de son entrevue avec le Roi Nidas dans son Palais des Vermeils.
        Beaucoup, parmi nous, doutaient de son histoire ; mais nous devions admettre que ce Kama frappé du
        sceau d’Enutrosor lui avait apporté son lot de chance et de richesses.</p>

        <p>« C’est juste que j’ai un très mauvais pressentiment, dis-je afin de rompre le silence entre nous. Cette
        affaire de Zaap temporel ne me dit rien qui vaille, et Merise ne m’inspire pas confiance.</p>
        <p>- C’est une agent de Mustam, grogna Kamen en jetant un regard dérobé dans sa direction. Elle essayera
        probablement de nous assassiner pour s’attirer la gloire de cette mission, si nous réussissons. Mieux
        vaut l’avoir à l’œil.</p>
        <p>- Non, c’est autre-chose… Je n’arrive pas à l’expliquer. Elle me fait peur, Kamen, bien plus que les autres
        sramettes que j’ai fréquentées. Je perçois une noirceur chez elle, une sorte de colère enfouie
        profondément. Elle me terrifie.</p>
        <p>- Bah ! lança l’enutrof en haussant les épaules. C’est sans doute lié à ce mal mystérieux qui la ronge.
        Nous ne savons rien de cette Pestilence. C’est peut-être un genre de corruption.</p>
        <p>- Je doute qu’un démon de la Shukrute puisse déclencher une telle maladie, intervins-je. Le fléau dont
        elle souffre n’a rien à voir avec mon ressenti. Cette sramette a le cœur empli de ténèbres et de
        malveillance.</p>

        <p>Kamen scruta mon visage avec amusement, et me glissa à voix basse :</p>
        
        <p>- Est-ce que ça n’aurait pas plutôt un rapport avec la manière dont elle dévore Kuro du regard ? Je crois
        sentir chez toi un léger soupçon de jalousie à son égard.</p>

        <p>Je m’empourprai aussitôt, à mon grand désarroi.</p>

        <p>- Tu racontes n’importe-quoi, vieillard ! Kuro et moi sommes juste des amis, et je…</p>
        <p>- À d’autres, demoiselle Chantelune ! Tu peux jouer de la harpe et du basson autant que tu le voudras,
        mais ton pipeau ne marchera pas avec moi. Je t’ai vue quitter sa chambre à plusieurs reprises sur la
        pointe des pieds, une fois le soleil levé.</p>

        <p>Il ricana d’un air triomphal et m’envoya un coup de coude amical.</p>
        
        <p>- Ne t’en fais pas, gamine. Le vieux Kamen a les lèvres scellées, il sait garder un secret. Je suis heureux
        pour toi, Kirina. C’est un iop d’honneur.</p>
        
        <p>Je lui adressai un sourire complice et reconnaissant.</p>
        
        <p>- Merci, Kamen. Tu as vraiment un cœur d’or.</p>
        <p>- Attention, ma fille ! Ne dis jamais ça à un enutrof, il serait capable de se l’arracher pour l’enfermer
        dans un coffre ! »</p>

        <p>Nous rîmes de concert à ce trait d’esprit. J’éprouvais pour Kamen une affection sincère, malgré son
        caractère parfois un peu ronchon. Il était, en quelque sorte, l’aïeul bienveillant de notre famille, celui
        qui racontait des histoires en hiver autour d’un grand feu. C’était lui qui m’avait donné envie d’exercer
        ma profession, lui aussi qui m’avait offert ma première harpe, quand j’avais seulement douze ans. Mes
        parents avaient été tués quand je n’étais qu’une enfant, dans une attaque de gobelins et de karnes sur
        le Village d’Amakna. À cette époque, le fondateur de la Colossus m’avait recueillie et élevée comme sa
        fille, veillant à mes besoins et à mon éducation. Mais la figure paternelle qui manquait cruellement
        dans ma vie, je la retrouvais davantage dans les traits malicieux et ridés du vieil enutrof, dont j’étais
        devenue aussi proche que l’on pouvait l’être. Kamen n’avait jamais eu d’enfant au cours de sa longue
        existence, et je le soupçonnais de voir toujours en moi la fillette qu’il aurait rêvé de serrer dans ses
        bras.</p>

        <p>Nous poursuivîmes notre marche en silence, contournant la façade sud de la banque. C’était un
        bâtiment colossal installé au nord du quartier de la milice, doté de trois grandes portes monumentales.
        Même à cette heure matinale, plusieurs clients se pressaient devant les grilles, attendant avec
        impatience pour effectuer un dépôt ou un retrait. Des gardes de la cité, revêtus de leurs uniformes
        sombres et de leurs hallebardes, se dressaient immobiles le long des colonnes sculptées, dont les
        décors en relief évoquaient des démons Shushus en guerre contre des anges. Malgré tout, l’endroit
        restait étonnamment calme pour un jour de semaine, d’autant que la grande foire-aux-muldos devait
        s’ouvrir en début d’après-midi.</p>

        <p>« L’appel aux armes de Mustam a fait fuir les commerçants, m’expliqua Kamen lorsque je lui fis part
        de mes réflexions. Ils craignent que la cité entre en guerre.</p>
        <p>- Mais la proclamation n’a rien à voir avec un conflit militaire ! Intervins-je, déboussolée.</p>
        <p>- Toi tu le sais, mais les marchands l’ignorent. Les patrouilles bontariennes s’aventurent de plus en plus
        loin dans les Landes de Sidimote, ces derniers temps. Sans compter les attaques récurrentes contre
        nos percepteurs… Les rumeurs vont vite. »</p>
        
        <p>J’approuvai distraitement, sans quitter des yeux le petit groupe de clients qui patientaient sous la pluie
        devant les hautes grilles en fer forgé. Plusieurs d’entre eux étaient des intendants qui travaillaient dans
        les hôtels de vente de la cité, et qui profitaient de l’heure matinale pour venir encaisser les recettes de
        la veille.</p>

        <p>« Amayiro essaye de détourner notre attention en envoyant ses néophytes à nos frontières, intervint
        Kuro qui avait suivi la conversation. À mon avis, il veut éloigner les soldats Brakmariens du Zaap
        temporel.</p>
        <p>- Mais qu’aurait-il à y gagner ? Il risque tout de même de déclencher une guerre !</p>
        <p>- Je l’ignore, avoua Kuro en haussant les épaules. Les intentions du roi de Bonta n’appartiennent qu’à
        lui. Amayiro est son laquais, alors il obéira à son suzerain. S’il découvre que nous sommes en route
        pour Astrub, il fera tout pour nous empêcher d’atteindre la cité.</p>
        <p>- Les Bontariens doivent en savoir davantage sur ce Zaap qu’ils ne veulent bien l’avouer, grogna Kamen
        en frappant du pied dans une pierre.</p>
        <p>- Ça ne me surprendrait guère, acquiesça Kuro. Si Merise a survécu à la Pestilence, il est fort possible
        qu’un espion d’Amayiro soit revenu en vie pour lui faire son rapport.</p>
        <p>- Tu t’attends à ce que des soldats de Bonta nous barrent la route, pas vrai ?</p>
        
        <p>Le grand iop opina du chef.</p>

        <p>- Le général Krakdel a envoyé ses éclaireurs dans les landes de Sidimote. Ils ont déjà capturé plusieurs
        aspirants de la cité des Anges. À l’heure qu’il est, Bout-de-Chair doit les interroger. Nous en saurons
        peut-être plus. »</p>
        
        <p>Je réprimai un frisson. Bout-de-Chair était le grand maître de la corporation des bouchers, mais ce
        surnom ne lui venait pas tant de son aptitude à travailler la viande que de son talent à torturer les
        prisonniers. Il faisait partie des plus proches collaborateurs d’Oto Mustam, mais rien chez cet individu
        sinistre ne m’inspirait la moindre sympathie. Mieux valait éviter de croiser son chemin si l’on tenait à
        conserver sa dentition ou ses extrémités.</p>
        
        <p>« On dirait que le vieux Mustam est prêt à tout pour conserver le secret de notre mission, remarqua
        Kamen. Je parierais le contenu de mon havre-sac qu’il ne nous a pas dit tout ce qu’il sait !</p>
        
        <p>Je souris face à la perspicacité de notre aîné.</p>

        <p>- C’est probable, en effet. Quoi qu’il en soit, nous allons être rapidement fixés. Nous arrivons. »</p>
        
        <p>Nous nous tûmes de concert en découvrant au coin de la rue la silhouette imposante et menaçante de
        la milice de Brakmar. Il s’agissait d’un immense bâtiment de forme rectangulaire, qui s’élevait comme
        une flèche en direction du ciel obscur de la cité. Seule la Tour des Ordres le dépassait dans sa hauteur.
        En son sommet trônait une statue monumentale à l’effigie d’un dragon hurlant, que certains
        associaient à la figure tutélaire et protectrice du démon Djaul. Il encadrait de ses ailes tombantes les
        deux portes de l’édifice et écrasait les visiteurs de sa prestance. De chaque côté, le fronton était
        soutenu par des amas de roche volcanique, dont l’aspect poreux et irrégulier suggérait qu’il s’agissait
        de stalagmites nées d’une ancienne éruption et non d’une construction douzienne. Les grands battants
        en obsidienne avaient été renforcés de charmes glyphiques qui rougeoyaient dans la nuit ; la légende
        voulait que seuls les véritables disciples de Djaul soient autorisés à les franchir. Enfin, ce spectacle
        grandiose était complété par la forme du fronton lui-même, qui ne ressemblait en rien aux colonnades
        de la banque : l’ensemble évoquait la gueule rugissante d’un démon à l’intérieure de laquelle il fallait
        se jeter. En s’approchant plus près, on pouvait en effet distinguer les prémices d’un escalier qui
        s’enfonçait sous terre. Le cœur serré, nous pénétrâmes un par un entre les mâchoires de la bête, et
        rapidement l’obscurité nous engloutit tandis que nous progressions vers le cœur du royaume d’Oto
        Mustam.</p>

        <p>Comme chaque fois que j’empruntais cet escalier taillé à même la roche, une sensation d’oppression
        m’envahit. Je n’étais pas vraiment sujette à la claustrophobie ou à la peur des endroits sombres, bien
        que l’humidité et la noirceur excessive des lieux ne me laissât pas de marbre. En réalité, il s’agissait surtout d’une sourde appréhension à l’idée
        de rencontrer à nouveau le propriétaire de ces lieux. Car, aussi effroyable et imposante que fût la façade de la milice de Brakmar, elle n’égalait
        en rien la terreur viscérale qu’éveillait chez moi notre sinistre maître.</p>
        
        <p>Comme nous descendions vers les entrailles de la cité, il devint évident que je n’étais pas la seule de
        notre groupe à être intimidée. De temps à autres, la lumière vacillante d’une torche me permettait
        d’entrevoir mes compagnons, et je surpris Guenon et Yudaï en train de frissonner. Pourtant,
        contrairement à une grotte ordinaire où la température chutait lorsqu’on se rapprochait des
        profondeurs, la chaleur ici devenait à chaque pas plus étouffante. Brakmar avait été bâtie tout entière
        sur une véritable fournaise, à l’emplacement du cratère de l’immense volcan qui bordait autrefois la
        frontière sud des landes de Sidimote. Jadis, la milice et les geôles se trouvaient à la surface et formaient
        un ensemble de dépendances qui jouxtaient la Tour des Ordres dans le quartier du Zaap. Mais Oto
        Mustam, lorsqu’il prit le pouvoir, décida d’abandonner les anciens bâtiments pour s’installer à l’étage
        le plus enfoui de la cité, jusqu’à effleurer les rivières de lave souterraines qui continuaient de s’écouler
        en provenance des Marches Magmatiques. Ainsi, disait-il, la chaleur infernale qu’il se contraignait à
        supporter nuit et jour renforçait son caractère, et venait à bout du plus endurci des prisonniers. Le
        bâtiment extérieur orné du dragon hurlant n’était donc qu’une coquille vide destinée à impressionner
        le tout-venant et à écraser de sa masse gigantesque l’architecture de la ville.</p>

        <p>Nous arrivâmes finalement dans une pièce haute de plafond et décorée de tentures rouges et ocres,
        au fond de laquelle se dressait un trône. Celui-ci était entièrement sculpté en bois d’ébène et en
        obsidienne, il s’ornait du visage de centaines de démons grimaçants. Comme à chacune de mes visites
        dans la milice, il demeurait vide : la légende voulait qu’il ait été façonné par le démon Djaul avec le feu
        de Volkaragnar en personne, et nul n’osait s’asseoir dessus, pas même le maître absolu de la ville.</p>

        <p>Or, justement, Oto Mustam nous observait.</p>

        <p>Il se tenait debout au pied de l’estrade, le dos bien droit, et dardait sur notre petit groupe un regard
        malsain. À ses côtés, je reconnus deux autres membres de notre communauté. Le premier était un
        grand disciple de Feca au sourire avenant et à l’œil pétillant d’intelligence. Il avait les cheveux d’un noir
        de jais noués en catogan, des pupilles d’un bleu clair comme les glaciers de Frigost, et une barbe de
        quelques jours s’étoffait au bas de son visage et le long de son cou. Comme à son habitude, Entrail
        était vêtu entièrement de brun, sa couleur fétiche ; il portait en travers de son dos un large bâton
        sculpté au pommeau orné d’un rubis, au-dessus d’un bouclier frappé des armoiries de la guilde. Mais
        celle qui retint mon attention se tenait à sa gauche, bras croisés, et nous lança un clin d’œil complice
        à notre arrivée. Je retins un hoquet de surprise, et entendis clairement Kamen pousser un sifflement
        admiratif dans mon dos. Toute bardée de mauve et de blanc, arborant fièrement la croix rouge du dieu Iop sur sa poitrine, Celexane avait fière
        allure. Elle avait hérité du regard cristallin de son père, mais la longue chevelure d’un roux flamboyant qui tombait sur ses épaules lui venait
        incontestablement de sa mère. Cela faisait bientôt quatre ans que je ne l’avais pas croisée, et l’adolescente rebelle et capricieuse
        qui faisait les quatre-cents coups au QG de la Colossus était devenue une jeune et séduisante iopette.
        En l’observant de plus près, je remarquai avec fierté la broche qui épinglait sa cape en-dessous de son
        épaule droite. Elle représentait deux ailes de démon rougeoyantes ornées de crocs, signe de sa récente
        montée en grade au sein de l’Ordre du Cœur Saignant de Brakmar. C’était la caste des guerriers, et les
        plus talentueux d’entre eux devenaient les commandants militaires de la cité. En comparant la fibule
        de Celexane à celle d’Entrail, je réalisai que notre apprentie avait réussi à atteindre un grade supérieur
        à celui de son père, dont le visage rayonnait de fierté.</p>

        <p>« Par la musette d’Enutrof ! S’exclama Kamen à voix basse. Si jeune, et déjà admise dans les Chevaliers
        du Désespoir ! Tu avais raison, Kuro. Cette petite est remarquable.</p>
        <p>- Le mérite en revient à Entrail, répondit notre chef avec satisfaction. Il l’a bien formée.</p>
        <p>- Tu es trop modeste, mon ami. Je suis sûr que tu as fait un excellent professeur.</p>
        
        <p>Kuro lui adressa un regard rieur, et répliqua d’un ton goguenard :</p>

        <p>- Elle a appris auprès des meilleurs. Dommage pour toi, tu n’en fais plus partie ! »</p>
        
        <p>Il se tut subitement tandis que nous avancions en direction d’Oto Mustam, mais Kamen ne put
        s’empêcher de lui asséner un coup de pelle amical dans le dos. Le maître de la milice fronça les sourcils
        en le voyant, et ce geste anodin suffit à rendre l’atmosphère plus froide que le souffle d’un Armutin.</p>

        <p><strong>« Vous avez presque deux heures de retard !</strong> Remarqua-t-il d’une voix grave. <strong>Décidément, l’indiscipline
        de votre guilde frôle l’insubordination.</strong></p>
        
        <p>Il marqua une pause, jeta à Kuro un regard flamboyant, et un sourire torve apparut au coin de ses
        lèvres.</p>
  
        <p><strong>- Je dois dire que ça me plaît. Brakmar devrait compter plus de soldats dans votre genre. »</strong></p>

        <p>Il éclata d’un rire sinistre qui résonna en écho dans les salles souterraines, avant de claquer
        brusquement des doigts. Comme s’ils avaient attendu notre arrivée, deux Iops particulièrement
        musclés firent leur apparition. Ils portaient à bout de bras un coffre en métal noir, qu’ils déposèrent
        lourdement aux pieds du maître de la milice. Mustam leur fit signe de s’en aller, et souleva le couvercle.</p>

        <p>« Par la barbe d’Enutrof ! » Jura Kamen en apercevant son contenu.</p>
        
        <p>Je me pressai un peu plus avant pour voir, et retins moi aussi une exclamation. Il y avait là une quantité
        de pièces d’or telle que je n’en avais jamais vue de ma vie. Le caisson en était plein à ras-bord, et elles
        scintillaient doucement à la lueur des flambeaux et des torchères qui ornaient les murs. Désormais
        certain d’avoir capté notre attention, Mustam désigna son précieux trésor.</p>

        <p><strong>« Voici votre récompense si vous réussissez cette mission</strong>, grogna-t-il. <strong>Un million de kamas pour chacun
        d’entre vous. »</strong></p>
        
        <p>Il referma le coffret d’un coup de pied dans le couvercle, et nous dévisagea tour à tour avec insistance.
        Comme chacun de mes camarades, j’inclinai délicatement la tête en signe de respect, ce qui ne
        m’empêcha pas de frissonner lorsque je sentis le poids de son regard qui s’arrêtait sur moi. Il fit un pas
        en avant, et un chuintement métallique retentit à mes oreilles. Avec horreur, je vis approcher
        l’extrémité de son sabre, dont il vint loger la pointe juste en-dessous de mon menton. D’une légère
        pression, il me força à relever la tête pour examiner mon visage. Mon corps tout entier était parcouru
        de tremblements incontrôlables, et des larmes perlèrent au coin de mes yeux. Je déglutis avec
        difficulté, rassemblant tout mon courage pour ne pas défaillir.</p>
 
        <p><strong>« Recalée !</strong> aboya Mustam de sa voix dure. <strong>J’ai demandé une troupe de guerriers, pas une musicienne
        pleurnicharde. »</strong></p>
  
        <p>Du coin de l’œil, je vis Kuro se raidir et serrer les dents. Son poing se crispa autour du pommeau de
        son épée, mais il ne réagit pas. Merise, de son côté, avait toujours aux lèvres son sourire cruel. Il
        semblait m’être destiné, et je n’en comprenais que trop bien le message. « Retourne à tes dièses et à
        tes croches, morveuse. Laisse faire les professionnels. » Pourtant, ce fut elle qui osa prendre la parole
        pour s’opposer à notre maître.</p>
        
        <p>« La joueuse de harpe vient avec nous, Mustam. Si elle reste, je reste aussi. Et sans guide, personne ne
        part. »</p>

        <p>Le chef de la Milice se figea, et je vis distinctement sa lèvre inférieure trembler sous l’effet de la colère.
        Tout le monde retint son souffle, et Entrail se prépara à invoquer ses Armures. Ces derniers temps, il
        était devenu rare qu’Oto Mustam massacre l’un de ses sbires dans un accès de rage. Mais nous
        connaissions tous l’étendue que pouvait atteindre sa fureur, en particulier quand on osait lui tenir tête
        de la sorte. Le métal froid de sa lame se fit plus pressant contre ma gorge, et je sentis sa morsure
        lorsqu’il entailla ma peau. Du sang perla le long de mon cou, et cette fois je ne pus retenir mes larmes.
        Je fis un pas en arrière et, terrorisée, je me jetai dans les bras de Kuro pour laisser jaillir ma peur. Il me
        serra contre sa poitrine, et posa sur le maître de la cité un regard assassin.
        À ses côtés, Kamen glissa une main à l’intérieur de sa musette et l’autre sur le manche de sa pelle, prêt à s’interposer. Guenon
        se raidit également, et je le surpris en train de jouer nerveusement avec la capsule de déploiement de
        sa Harponneuse. Derrière lui, Yudaï adopta imperceptiblement une garde de combat, et glissa sa main
        gauche dans son dos à la recherche de l’épée Kukri qui pendait à sa ceinture. Paniquée, j’enfouis mon
        visage contre le tabard de Kuro pour ne pas hurler. Si un seul d’entre eux osait lever la main contre
        Mustam…</p>

        <p><strong>« Que comptez-vous faire au juste, bande de larves ?</strong> s’écria celui-ci en faisant virevolter son sabre dans
        notre direction. <strong>M’attaquer ? Pour défendre cette eniripsa pathétique ?</strong></p>
 
        <p>Il éclata d’un rire sonore, et rengaina son arme.</p>

        <p><strong>- Bien</strong>, dit-il,<strong> il y a peut-être quelques guerriers courageux dans ce tas de bworks, finalement. C’est
        entendu. La musicienne sera du voyage.</strong></p>

        <p>Il se tourna vers Merise, et jeta sur elle un regard meurtrier.</p>

        <p><strong>- Quant à toi, misérable, ne me parle plus jamais sur ce ton. Je ne serai pas aussi clément la prochaine
        fois.</strong></p>

        <p>Il nous dévisagea une dernière fois à la volée, et tourna les talons.</p>

        <p>- Suivez-moi. J’ai quelque-chose à vous montrer. »</p>

        <p>Nous hésitâmes un instant avant de lui emboîter le pas en direction du trône de Djaul, contre lequel il
        se glissa. Du bout des doigts, il caressa la tête d’un démon cornu sculpté dans l’obsidienne, et un
        dispositif d’engrenages s’enclencha dans le sol de l’estrade sous nos pieds. Le socle massif du trône
        pivota d’une trentaine de centimètres, dévoilant une tenture rouge que Mustam écarta d’un geste vif.
        Nous le suivîmes alors dans un long couloir plongé dans l’obscurité, avant que la lueur vacillante d’une
        torche nous révèle un escalier. Nous remontions.</p>

        <p><strong>« Allez-y</strong>, ordonna-t-il. <strong>Je dois rester derrière pour verrouiller le passage. »</strong></p>

        <p>Un à un, nous attaquâmes l’ascension. Kuro passa le premier, suivi de près par Kamen qui serrait
        toujours le manche de sa pelle à s’en bleuir les phalanges. Vinrent ensuite Entrail et Celexane, et je
        leur emboitai le pas. Yudaï et Guenon fermaient la marche en silence. Le grand sacrieur n’avait pas dit
        un mot depuis notre arrivée, mais je devinais à son attitude un profond malaise. Lui non plus ne se
        sentait pas en sécurité à l’intérieur de la Milice, et la tension extrême qui s’était emparée de nous
        n’était pas encore retombée. Il me jeta un regard à la dérobée et s’approcha de mon épaule.</p>

        <p>« Ça va, Kirina ? chuchota-t-il. Tu n’es pas blessée ?</p>
        
        <p>D’un geste machinal, je passai ma main le long de mon cou et l’examinai à la lueur de la torche. Je
        constatai avec soulagement que ma blessure avait arrêté de saigner.</p>

        <p>- Non, rien de grave, lui répondis-je. Ce n’est qu’une petite entaille, et mes pouvoirs de régénération
        font déjà leur travail.</p>
        <p>- Tant mieux, grogna Yudaï. Si cet immonde fils de trool t’avait fait du mal, je jure sur la déesse Sacrieur
        que je lui aurais fait manger ses propres viscères pour le déjeuner ! »</p>

        <p>Il m’adressa un sourire crispé, et me fit signe de grimper à la suite des autres. Un peu plus loin dans
        notre dos, nous entendîmes le raclement du trône qui retrouvait sa place, et le pas martial du chef de
        la milice qui nous rejoignait. Le passage ne s’éleva pas très longtemps, et après une trentaine de
        marches environ nous retrouvâmes un couloir qui s’étendait vers le nord. Kuro et Kamen prenaient de
        l’avance, et je dus forcer l’allure pour ne pas me faire distancer. J’avais presque complètement perdu
        mes repères désormais, mais une chose était certaine : nous nous éloignions des bâtiments de la
        milice.</p>

        <p>« Dis-moi, Yudaï… Où mène ce chemin, à ton avis ?</p>
        <p>- Aucune idée. Les adeptes de l’Esprit Malsain racontent qu’il existe tout un dédale de tunnels sous la
        cité sombre, creusé dans les galeries de l’ancien volcan. Du temps de la Tétrarchie, les contrebandiers
        s’en servaient pour faire entrer leurs marchandises en toute discrétion, mais la plupart de ceux qui
        conduisent à l’extérieur de la ville ont été condamnés. J’imagine qu’Oto Mustam en a conservé
        quelques-uns pour se déplacer sans être vu.</p>

        <p>J’acquiesçai distraitement, non sans m’étonner d’une telle érudition de la part de mon compagnon.</p>

        <p>- C’est Ori qui me l’a raconté, précisa-t-il en haussant les épaules. Tu connais sa fascination pour
        l’histoire de la cité. »</p>

        <p>Voilà qui faisait désormais plus de sens, et je hochai la tête en silence. Nous marchâmes ainsi pendant
        environ dix minutes, sans autre compagnon que l’écho de nos pas qui se répercutait de manière
        lugubre. Enfin, au détour d’un virage, nous aperçûmes une lueur rouge qui tremblotait à l’extrémité
        du couloir. Il s’agissait en réalité d’une grande lampe à huile sculptée dans un crâne humain, alimentée
        par un combustible qui dégageait des flammes couleur carmin. Elle reposait sur un tabouret en bois
        sombre, et n’éclairait rien d’autre qu’un grand mur de pierre parfaitement hermétique. Hormis cela,
        rien. Le souterrain se terminait en impasse.</p>

        <p>« Qu’est-ce que ça signifie ? Demandais-je à Guenon et Yudaï, qui semblaient aussi surpris que moi.</p>
        <p>- Ecartez-vous un peu, nous répondit Kuro. Vous allez voir. »</p>

        <p>Lui-même s’avança plus près du flambeau, jusqu’à repérer un étrange interstice dans le mur. Avec un
        sourire approbateur, il dégrafa la broche de sa cape et la glissa à l’intérieur. Elle correspondait
        parfaitement à la forme de l’aspérité. Lorsqu’elle disparut à mes yeux, j’entendis un autre jeu
        d’engrenages se mettre en place, et le pan de mur entier pivota. Comme au sein de la Milice un peu
        plus tôt, il dévoila une grande tenture couleur sanguine, que nous franchîmes un à un avec
        appréhension.</p>

        <p>« Seuls les Héros du Cœur Saignant peuvent actionner ces serrures, m’expliqua Kuro quand j’arrivais à
        sa hauteur. Nous sommes quelque-part dans la Tour des Ordres. »</p>

        <p>Effectivement, je découvris une pièce aux murs de granit sculpté, parcourue d’un grand tapis rouge et
        dont même les plus infimes recoins abritaient des étagères débordant de registres et de rouleaux de
        parchemin. Au centre, une grande table en bois d’ébène supportait une carte du Monde des Douze
        d’une taille tout à fait impressionnante. Des épingles de couleur jalonnaient les possessions respectives
        de Bonta et de Brakmar, et plusieurs types de pions semblaient correspondre à des mouvements
        d’armée. L’ensemble donnait l’impression d’un jeu de stratégie à grande échelle, à ceci près que cette
        représentation n’avait rien d’imaginaire. Chaque tour, chaque soldat et chaque épingle représentés
        sur la mappemonde symbolisaient une partie de nos guerriers, et du mouvement de ces pièces pouvait
        dépendre la vie et la mort de beaucoup d’entre eux. Je frissonnai. Quel jeu terrible et cruel que la
        guerre, où des hommes et des femmes perdaient la vie et commettaient des atrocités pour
        s’approprier les biens des autres.</p>

        <p>Dans notre dos, le passage dérobé se referma avec un clang sonore, et nous sursautâmes à l’unisson.
        Oto Mustam venait de nous rejoindre dans la salle des cartes. Il nous fit signe d’avancer et de se
        regrouper autour de la mappemonde, sur la table centrale. De l’autre côté de la pièce, une tenture
        s’écarta alors pour dévoiler une iopette que je ne connaissais pas, aux cheveux d’un gris terne et au
        costume d’un brun délavé. Elle portait une tunique serrée au niveau des côtes par des lacets de cuir,
        frappée en son centre de la grande croix du Dieu Iop, complétée par des braies en lin tannées à l’ocre.
        Sur sa tête reposait un galurin rapiécé qui avait sans doute dépassé mon âge, autour duquel le
        chapelier avait accroché des dizaines de dents de crocodaille. Un curieux accessoire de mode qui ne la
        mettait pas du tout en valeur et qui m’arracha un sourire en coin. Pourtant, à voir les cicatrices que la
        nouvelle arrivante arborait sur son visage et sur ses bras, il ne s’agissait pas d’une prêtresse dévouée
        à l’Ordre de l’Œil Putride, qui passait ses journées enfermée dans la Tour à compulser des registres.
        Non, c’était une authentique guerrière, impression confirmée par le pommeau d’une dague-Erhy qui
        dépassait de son dos au niveau de sa ceinture. Elle pénétra dans la pièce d’un pas martial qui acheva de me convaincre, et vint se planter devant
        nous avec un regard hautain en abattant ses deux mains sur la table. Aussitôt je vis l’ensemble de mes compagnons s’incliner avec respect. Afin de 
        ne pas éveiller la colère de notre maître, je les imitai sans tarder.</p>

        <p><strong>« Bien, j’imagine qu’il est inutile de faire les présentations</strong>, grogna Mustam avec satisfaction.<strong> Comme vous
        le savez, Emma commande à l’ensemble de nos soldats et éclaireurs, ainsi qu’aux légions de Chaferfus de
        la ville. Elle connait les Landes de Sidimote mieux que quiconque, et va vous indiquer votre itinéraire. »</strong></p>

        <p>Je dévisageai soudain la iopette à la crinière de cendre avec une déférence nouvelle mêlée de crainte.
        Emma Sacre était la générale suprême de l’Ordre du Cœur Saignant, le bras armé de la cité de Brakmar.
        Cette femme qui se tenait devant nous était une légende. Ses exploits militaires étaient connus sur
        tout le continent, et il n’y avait guère que le célèbre Goultard pour rivaliser avec elle au panthéon des
        guerriers. Je me sentais brusquement honteuse de ne l’avoir pas identifiée dès son arrivée. Kamen
        m’adressa un coup de coude discret, et se pencha vers moi.</p>

        <p>« Tu vois les dents autour de son chapeau, gamine ? Un jour, Ori m’a raconté qu’Emma les arrache à
        mains nues dans la gueule des chefs crocodailles. Tous les ans, elle se rend dans les marécages pour
        ajouter un trophée sur son galurin. C’est un symbole de sa bravoure et de son courage, qui justifie son
        autorité. Il parait qu’elle tient plus à ce vieux chapeau qu’à sa propre vie. »</p>

        <p>Je l’écoutai avec intérêt, et rangeai précieusement ces informations dans un coin de ma mémoire. Il y
        avait là matière à composer une nouvelle chanson, et je comptais bien m’y atteler dès que j’aurais
        l’occasion de saisir ma plume. Mais, pour l’heure, c’était au tour d’Emma Sacre d’occuper le devant de
        la scène.</p>
        
        <p>« Approchez ! Ordonna-t-elle d’une voix forte. Je veux que tout le monde m’entende et puisse suivre
        mes indications sur la carte.</p>

        <p>Elle patienta quelques secondes tandis que nous nous pressions tous à ses côtés. Son regard voltigeait
        d’un bout à l’autre de notre groupe, et s’arrêta soudain lorsqu’elle reconnut le plus âgé d’entre nous.</p>
        
        <p>- Tiens donc ! Mais ne serait-ce pas ce cher Kamen ? Comment se porte ta carcasse d’ancêtre, vieux
        gredin ? Je croyais qu’on t’avait expédié en retraite forcée au Pays des Vermeils !</p>

        <p>L’enutrof rougit comme une tomate, eut un petit rire nerveux et répliqua sur un ton mi-figue, mi-raisin.</p>

        <p>- Il faut croire que le Roi Nidas n’a pas voulu de moi. Et ne te fais pas de soucis pour moi, ma fille. Je
        suis encore assez affûté pour te flanquer une solide déculottée, si l’envie m’en prends. »</p>

        <p>Emma Sacre partit d’un grand éclat de rire et lui adressa un sourire chaleureux. Ces deux-là se
        connaissaient, c’était une évidence ; à voir leur complicité, on aurait pu croire qu’ils avaient élevé des
        bouftous ensemble. Il faudrait que j’interroge Kamen à ce sujet pendant notre voyage.</p>

        <p>- Bien, reprit finalement la iopette lorsqu’elle retrouva son sérieux. Comme vous pouvez le constater,
        les Bontariens ont pris possession de la partie nord des Landes de Sidimote, jusqu’à la Baie de Cania.
        Ils ont établi un camp ici – elle planta une punaise de couleur bleue sur sa mappemonde pour illustrer
        sa démonstration –, juste en bordure du Village des Dopeuls. Amayiro a fait installer un Prisme
        d’Alliance dans cette zone, ce qui leur permet d’acheminer très rapidement des renforts depuis la Cité
        Blanche en cas d’attaque ou de tentative d’intrusion. De plus, leurs patrouilles s’aventurent
        régulièrement dans les Landes pour tenter de capturer nos espions. La nuit dernière, l’une d’entre elles
        a été repérée à quelques heures de marche de notre relai de diligences. Si nous ne les arrêtons pas
        très vite, le Zaap des Landes pourrait tomber entre leurs mains.</p>

        <p>- Donc, résuma Kuro, le passage par le Nord est surveillé. Qu’en est-il de l’ancienne mine de Kobalte
        qui permet de rejoindre la Forêt Maléfique à l’Est ?</p>
        <p>- Ce tunnel est abandonné depuis des années, expliqua Emma Sacre. Un groupe de bandits, connu
        comme la Confrérie des Mineurs Sombres, en a pris possession. Ils n’hésitent pas à dépouiller tous les
        voyageurs qui tentent la traversée de leurs terres, et sont suffisamment nombreux et armés pour tenir
        nos soldats en échec. Vous n’en viendriez pas à bout.</p>
        
        <p>Il y eut un silence lourd de sens, pendant lequel nous examinions la carte avec attention. Ce fut Guenon
        qui, de son œil affuté, remarqua un autre chemin.</p>

        <p>- Quel est ce sentier, qui semble traverser la Montagne des Koalaks ? Je ne crois pas le connaître.</p>
        
        <p>La grande maîtresse de l’Ordre du Cœur Saignant lui jeta un regard pénétrant.</p>

        <p>- Toujours aussi vif d’esprit, aspirant Guenon. Il s’agit d’un chantier sur lequel nous travaillons en secret
        depuis un bon moment. Nous exploitons les gobelins-sapeurs que nous capturons sur l’île de Sakaï
        pour aménager un escalier dans les montagnes. Si tout se passe comme prévu, il nous permettra
        d’accéder directement au Village des Eleveurs, au nez et à la barbe des Bontariens. Mais il n’est pas
        encore achevé.</p>
        <p>- Donc, comprit Yudaï avec un frémissement, il ne reste que les Hauts des Hurlements…</p>
        <p>- Effectivement, trancha Emma Sacre. Votre seule chance d’atteindre Astrub sans vous faire repérer,
        c’est de longer les contreforts de la montagne vers le Nord sur un territoire que les Bontariens évitent
        comme la peste. Les Hauts des Hurlements. »</p>

        <p>Mon sang se glaça à l’évocation de notre futur itinéraire, et je compris d’un seul regard que mes
        compagnons n’en menaient pas large non plus. Les Hauts des Hurlements tenaient leur nom des
        maraudeurs mulous qui parcouraient cette partie des Landes à la nuit tombée. Ces créatures
        ressemblaient à des loups gigantesques, mais étaient dotées d’une rare intelligence. Ils évoluaient
        parfois debout comme des bipèdes, mais atteignaient également des vitesses stupéfiantes à quatre
        pattes. Un mulou adulte rivalisait sans mal avec un tabi ou une dragodinde à la course, ôtant tout
        espoir d’échappatoire à sa proie. Ils étaient capables de bonds prodigieux et possédaient des griffes et
        des crocs qui n’avaient rien à envier à nos lames en acier trempé. Les contes pour enfants qui parlaient
        du « grand méchant mulou » étaient bien loin de la réalité et ne servaient qu’à effrayer les mauvais
        garnements. Ceux qui avaient affronté un mulou, un vrai, se réveillaient encore des années plus tard
        au milieu de la nuit en tremblant, croyant avoir entendu au loin l’écho de leurs hurlements. 
        Mais il ne s’agissait pas seulement de bêtes sauvages qui s’en prenaient aux voyageurs égarés pour en
        faire leurs repas. Les mulous se déplaçaient en meutes, obéissaient à une stricte hiérarchie et
        patrouillaient consciencieusement leur territoire comme le ferait une armée de métier. Cette
        organisation rigoureuse faisait d’eux un danger particulièrement mortel : les maraudeurs mulous
        étaient en effet capables de tendre des embuscades, d’effectuer des replis stratégiques et de prendre
        des groupes armés à revers dans les gorges de leurs vallées escarpées. Un seul de ces féroces
        prédateurs pouvait tenir tête à une dizaine de soldats bien équipés et entraînés. Et, chaque soir à la
        lumière de la lune, il en venait des milliers dans les collines qui formaient les contreforts des
        montagnes.</p>

        <p>Autrement dit, franchir leur territoire était une mission suicide.</p>

        <p>- Vous n’y pensez pas, générale ! S’étrangla Kuro, pourtant le plus brave d’entre nous. Autant se jeter
        sans arme ni armure dans la fosse d’un trool ! C’est de la folie !</p>
        
        <p>Emma Sacre ricana, et posa un regard dur sur notre chef en haussant les épaules.</p>

        <p>- C’est précisément pour cette raison que j’ai choisi cet itinéraire. Les Bontariens ne vous attendront
        pas de l’autre côté des collines.</p>
        <p>- Encore faudrait-il qu’on y parvienne en un seul morceau ! Grogna Kamen, qui caressait nerveusement
        sa barbe. J’aimerais bien t’y voir, Emma, dans les Hauts à la tombée de la nuit. M’est avis que tu aurais
        tôt fait de revenir à Brakmar, et qu’il faudrait t’enfermer dans une de nos cellules pour calmer tes
        attaques de panique.</p>
        
        <p>La grande iopette balaya l’argument du vieil enutrof d’un revers de main.</p>
        <p>- Il existe un moyen de les traverser sans danger, énonça-t-elle. Pour cela, vous aurez besoin d’un guide
        qui connait la région comme sa poche et qui choisira pour vous les sentiers sûrs. Adressez vous à Rodak
        le Pagneul et présentez-lui le sceau de la cité. Son clan habite au pied des montagnes, et fait face
        depuis des décennies aux assauts des maraudeurs mulous sur leurs terres. Brakmar lui a promis un
        renfort de guerriers s’il accepte de vous conduire sains et saufs jusqu’aux Plaines de Cania.</p>
        <p>- Même avec le meilleur des guides, intervint Guenon d’un air sceptique, les mulous renifleront notre
        odeur à des kilomètres de distance. Ils nous traqueront et nous mettront en pièces.</p>
        <p>- C’est là que les talents de Merise vous seront utiles, contra Emma Sacre avec entrain. Ce n’est pas
        seulement une espionne hors pair au service de la cité ; elle maîtrise également l’art du sortilège de
        Brume à la perfection. Elle vous permettra de passer inaperçus. »</p>

        <p>Je tournai le regard en direction de la sramette qui se tenait en retrait, négligemment appuyée contre
        une étagère. Comme toujours, un frisson d’effroi parcourut ma colonne vertébrale lorsque mes yeux
        se posèrent sur son masque et sur l’ample capuche qui masquait ses traits. Elle m’était affreusement
        antipathique, et je ne lui faisais pas confiance. Mais elle représentait notre seule chance de traverser
        les Hauts des Hurlements en vie. Je serrai les poings à m’en bleuir les phalanges.</p>
        
        <p>- D’accord, céda Kuro avec un soupir. Tu as l’air sûre de ton fait, Emma, et tu as toute ma confiance.
        Nous tenterons notre chance par les collines.</p>
        
        <p>Il se tourna vers Oto Mustam, et ajouta d’une voix emprunte de respect :</p>

        <p>- Avec votre permission, maître, Yudaï et Guenon nous accompagneront dans ce voyage. Une fois
        parvenus à Astrub, ils obliqueront vers le Sud pour rejoindre ceux de nos guerriers qui défendent nos
        percepteurs en Amakna.</p>
        <p><strong>- Accordé</strong>, grogna Mustam de sa voix caverneuse.<strong> Mais il manquera deux personnes à votre groupe pour
        franchir le Zaap Temporel.</strong></p>
        <p>- J’ai fait prévenir nos amis Sheicko et Marsh par courrier, expliqua Kuro. Ils nous rejoindront
        directement dans la cité des mercenaires. Ce sont des alchimistes de talent, qui nous seront précieux
        pour contrer les effets de la Pestilence. »</p>
        
        <p>Notre terrible maître obtempéra et nous congédia, mettant fin à notre réunion. Le cœur lourd et
        tremblant d’appréhension, je passai en revue le visage de mes compagnons d’infortune. Kuro, notre
        valeureux chef, et Kamen l’enutrof au cœur d’or. Entrail et Celexane, père et fille, deux courageux
        guerriers du Cœur Saignant. Guenon le steamer, redoutable par sa capacité à déployer des tourelles
        mécaniques sur le champ de bataille. Merise, sramette mystérieuse et ténébreuse, mais dotée de grands pouvoirs et d’une formation d’assassin. 
        Et Yudaï pour finir, le grand sacrieur silencieux, qui n’hésiterait pas une seconde à sacrifier sa vie pour sauver les nôtres. 
        Au milieu de tous ces combattants d’exception, je me sentais presque autant à ma place qu’un poisskaille givré dans les filets d’un
        pêcheur. Mais, au fond de mon cœur, je me sentais rassurée de les avoir à mes côtés.</p>

        <p>Ensemble, nous avions peut-être une chance de traverser les Hauts des Hurlements en vie.</p>
